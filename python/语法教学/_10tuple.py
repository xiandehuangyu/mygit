#元组的 “不可变” 是指：元组中每个元素的「引用（内存地址）」不可变,
#即元组只能固定指向最初的那些对象，
#不能更换指向的对象（比如把元组里的列表换成另一个列表，或换成数字）。
#不可变对象（int/str/tuple）：创建后，自身的内容 / 值无法修改，修改会生成新对象（内存地址改变）。
#比如 a = 1 → a = 2，其实是变量 a 从指向 1 的内存地址，改成了指向 2 的内存地址，1 本身没被修改。
#可变对象（list/dict/set）：创建后，自身的内容 / 值可以修改，且内存地址不变。
